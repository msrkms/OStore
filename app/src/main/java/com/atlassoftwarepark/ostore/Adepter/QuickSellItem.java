public class QuickSellItem {
    private String QS_invoiceNo;
    private String QS_sellDate;
    private String QS_amount;
    private String QS_action;

    public String getQS_invoiceNo() {
        return QS_invoiceNo;
    }

    public void setQS_invoiceNo(String QS_invoiceNo) {
        this.QS_invoiceNo = QS_invoiceNo;
    }

    public String getQS_sellDate() {
        return QS_sellDate;
    }

    public void setQS_sellDate(String QS_sellDate) {
        this.QS_sellDate = QS_sellDate;
    }

    public String getQS_amount() {
        return QS_amount;
    }

    public void setQS_amount(String QS_amount) {
        this.QS_amount = QS_amount;
    }

    public String getQS_action() {
        return QS_action;
    }

    public void setQS_action(String QS_action) {
        this.QS_action = QS_action;
    }
}
